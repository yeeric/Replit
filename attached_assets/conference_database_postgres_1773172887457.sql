DROP TABLE IF EXISTS SpeaksAt CASCADE;
DROP TABLE IF EXISTS Attends CASCADE;
DROP TABLE IF EXISTS MemberOfCommittee CASCADE;
DROP TABLE IF EXISTS JobAd CASCADE;
DROP TABLE IF EXISTS Speaker CASCADE;
DROP TABLE IF EXISTS Sponsor CASCADE;
DROP TABLE IF EXISTS Professional CASCADE;
DROP TABLE IF EXISTS Student CASCADE;
DROP TABLE IF EXISTS Attendee CASCADE;
DROP TABLE IF EXISTS Session CASCADE;
DROP TABLE IF EXISTS HotelRoom CASCADE;
DROP TABLE IF EXISTS SubCommittee CASCADE;
DROP TABLE IF EXISTS CommitteeMember CASCADE;
DROP TABLE IF EXISTS Company CASCADE;

DROP TYPE IF EXISTS attendee_type CASCADE;
DROP TYPE IF EXISTS sponsor_level CASCADE;

CREATE TYPE attendee_type AS ENUM ('Student', 'Professional', 'Sponsor');
CREATE TYPE sponsor_level AS ENUM ('Platinum', 'Gold', 'Silver', 'Bronze');

-- ============================================================
-- BASE TABLES
-- ============================================================

CREATE TABLE Company (
    CompanyID INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    CompanyName VARCHAR(100) NOT NULL
);

-- ============================================================
-- COMMITTEE MEMBER
-- ============================================================

CREATE TABLE CommitteeMember (
    MemberID INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    FirstName VARCHAR(100) NOT NULL,
    LastName VARCHAR(100) NOT NULL
);

CREATE TABLE SubCommittee (
    CommitteeID INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    CommitteeName VARCHAR(100) NOT NULL,
    ChairMemberID INT NOT NULL,
    FOREIGN KEY (ChairMemberID) REFERENCES CommitteeMember(MemberID)
);

CREATE TABLE HotelRoom (
    RoomNumber INT PRIMARY KEY,
    NumberOfBeds INT NOT NULL
);

CREATE TABLE Session (
    SessionID INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    SessionName VARCHAR(150) NOT NULL,
    Date DATE NOT NULL,
    StartTime TIME NOT NULL,
    EndTime TIME NOT NULL,
    RoomLocation VARCHAR(100) NOT NULL
);

-- ============================================================
-- ATTENDEE HIERARCHY (ISA / Specialization)
-- ============================================================

CREATE TABLE Attendee (
    AttendeeID INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    FirstName VARCHAR(50) NOT NULL,
    LastName VARCHAR(50) NOT NULL,
    Email VARCHAR(100) NOT NULL UNIQUE,
    AttendeeType attendee_type NOT NULL,
    Fee NUMERIC(10,2) GENERATED ALWAYS AS (
        CASE AttendeeType
            WHEN 'Student' THEN 50.00
            WHEN 'Professional' THEN 100.00
            WHEN 'Sponsor' THEN 0.00
        END
    ) STORED
);

CREATE TABLE Student (
    AttendeeID INT PRIMARY KEY,
    RoomNumberStaysIn INT,
    FOREIGN KEY (AttendeeID) REFERENCES Attendee(AttendeeID)
        ON DELETE CASCADE,
    FOREIGN KEY (RoomNumberStaysIn) REFERENCES HotelRoom(RoomNumber)
        ON DELETE SET NULL
);

CREATE TABLE Professional (
    AttendeeID INT PRIMARY KEY,
    FOREIGN KEY (AttendeeID) REFERENCES Attendee(AttendeeID)
        ON DELETE CASCADE
);

CREATE TABLE Sponsor (
    AttendeeID INT PRIMARY KEY,
    SponsorLevel sponsor_level NOT NULL,
    EmailsSent INT NOT NULL DEFAULT 0,
    MaxEmailsAllowed INT GENERATED ALWAYS AS (
        CASE SponsorLevel
            WHEN 'Platinum' THEN 5
            WHEN 'Gold' THEN 4
            WHEN 'Silver' THEN 3
            WHEN 'Bronze' THEN 0
        END
    ) STORED,
    CompanyID INT NOT NULL,
    FOREIGN KEY (AttendeeID) REFERENCES Attendee(AttendeeID)
        ON DELETE CASCADE,
    FOREIGN KEY (CompanyID) REFERENCES Company(CompanyID)
        ON DELETE CASCADE
);

-- ============================================================
-- SPEAKER (can also be an Attendee)
-- ============================================================

CREATE TABLE Speaker (
    SpeakerID INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    FirstName VARCHAR(50) NOT NULL,
    LastName VARCHAR(50) NOT NULL,
    AttendeeID INT NULL,
    FOREIGN KEY (AttendeeID) REFERENCES Attendee(AttendeeID)
        ON DELETE SET NULL
);

-- ============================================================
-- JOB AD (posted by Company, 1:N)
-- ============================================================

CREATE TABLE JobAd (
    JobTitle VARCHAR(150) NOT NULL,
    Location VARCHAR(150) NOT NULL,
    City VARCHAR(100) NOT NULL,
    Province VARCHAR(100) NOT NULL,
    PayRate NUMERIC(10,2) NOT NULL,
    PostedByCompanyId INT NOT NULL,
    PRIMARY KEY (PostedByCompanyId, JobTitle),
    FOREIGN KEY (PostedByCompanyId) REFERENCES Company(CompanyID)
        ON DELETE CASCADE
);

-- ============================================================
-- RELATIONSHIP TABLES (M:N)
-- ============================================================

CREATE TABLE Attends (
    AttendeeID INT NOT NULL,
    SessionID INT NOT NULL,
    PRIMARY KEY (AttendeeID, SessionID),
    FOREIGN KEY (AttendeeID) REFERENCES Attendee(AttendeeID)
        ON DELETE CASCADE,
    FOREIGN KEY (SessionID) REFERENCES Session(SessionID)
        ON DELETE CASCADE
);

CREATE TABLE SpeaksAt (
    SpeakerID INT NOT NULL,
    SessionID INT NOT NULL,
    PRIMARY KEY (SpeakerID, SessionID),
    FOREIGN KEY (SpeakerID) REFERENCES Speaker(SpeakerID)
        ON DELETE CASCADE,
    FOREIGN KEY (SessionID) REFERENCES Session(SessionID)
        ON DELETE CASCADE
);

CREATE TABLE MemberOfCommittee (
    MemberID INT NOT NULL,
    CommitteeID INT NOT NULL,
    PRIMARY KEY (MemberID, CommitteeID),
    FOREIGN KEY (MemberID) REFERENCES CommitteeMember(MemberID)
        ON DELETE CASCADE,
    FOREIGN KEY (CommitteeID) REFERENCES SubCommittee(CommitteeID)
        ON DELETE CASCADE
);

